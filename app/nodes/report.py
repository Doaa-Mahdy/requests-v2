from langchain_core.messages import HumanMessage
from app.state import CaseState
from app.services.llm import llm_model


def report_node(state: CaseState) -> dict:
    evidence = state.get("evidence", {})
    reasoning = state.get("reasoning", {})
    text = state.get("text", "")
    inquiry_history = state.get("inquiry_history", [])

    prompt = f"""
أنت نظام تحليل نهائي متقدم لمؤسسة خيرية إنسانية.

مهمتك ليست تلخيص البيانات فقط،
بل "ربط جميع الأدلة" (نص + صور + نتائج بحث + ملاحظات) لفهم الحالة الحقيقية واتخاذ قرار إنساني دقيق.

━━━━━━━━━━━━━━━━━━━━━━
📌 المدخلات
━━━━━━━━━━━━━━━━━━━━━━
النص الأصلي: {{text}}
نتائج الاستدلال: {{reasoning}}
الأدلة المجمعة (نص/صور/بحث): {{evidence}}
سجل العمليات: {{inquiry_history}}

━━━━━━━━━━━━━━━━━━━━━━
🧠 الهدف الحقيقي
━━━━━━━━━━━━━━━━━━━━━━
أنت تبني "صورة واحدة موحدة للحالة".

يجب أن تقوم بـ:
- ربط كل قطعة معلومة بالأخرى
- اكتشاف التناقضات أو الفراغات
- تفسير المعنى الحقيقي خلف الطلب
- تحويل كل شيء إلى قرار إنساني واحد واضح

━━━━━━━━━━━━━━━━━━━━━━
🔗 طريقة التفكير (مهم جداً)
━━━━━━━━━━━━━━━━━━━━━━

1) ما الذي يقوله الشخص فعلياً؟ (Claim)
2) ماذا تقول الصور؟
3) ماذا تقول المعرفة الخارجية (Search)؟
4) هل هناك تناقض أو نقص؟
5) ماذا يعني كل ذلك مجتمعاً؟

━━━━━━━━━━━━━━━━━━━━━━
🧩 تحليل الربط (Core Task)
━━━━━━━━━━━━━━━━━━━━━━

- اربط المصطلحات غير المفهومة بما تم البحث عنه
- اربط الصور بالادعاء (هل تدعمه أم تنفيه؟)
- اربط التكاليف أو المعلومات الخارجية بالاحتياج الفعلي
- حدد هل المشكلة حقيقية أم مبالغ فيها أم غير موجودة

━━━━━━━━━━━━━━━━━━━━━━
🚨 قواعد صارمة
━━━━━━━━━━━━━━━━━━━━━━
- لا تعتمد على مصدر واحد فقط
- لا تفصل بين البيانات — يجب دمجها
- لا تترك أي معلومة بدون تفسير علاقتها بالحالة
- إذا كان هناك تضارب → اذكره صراحة

━━━━━━━━━━━━━━━━━━━━━━
🧠 منظور المؤسسة الخيرية
━━━━━━━━━━━━━━━━━━━━━━
فكر كموظف ميداني:
- هل هذا الشخص يحتاج مساعدة فعلية؟
- ما أسوأ سيناريو إذا أخطأنا؟
- هل الطلب طبي / مالي / طارئ / غير واضح؟
- هل يمكن أن يكون هناك سوء فهم؟

━━━━━━━━━━━━━━━━━━━━━━
🟩 أمثلة على الربط الصحيح

مثال 1:
نص: "House burned"
بحث: تكلفة إعادة بناء منزل مرتفعة
→ الاستنتاج: حالة طارئة → دعم إسكان عاجل

مثال 2:
نص: "medical clot"
بحث: clot قد يكون خطير أو بسيط حسب الحالة
→ الاستنتاج: يحتاج تقييم طبي → طارئ متوسط/عالٍ حسب الأعراض

مثال 3:
صورة: إصابة بسيطة
نص: طلب مساعدة مالية كبيرة
→ تضارب → تقليل مستوى الطوارئ + طلب تحقق إضافي

━━━━━━━━━━━━━━━━━━━━━━
📤 الإخراج (JSON فقط)
━━━━━━━━━━━━━━━━━━━━━━

{{
  "case_synthesis": "وصف موحد للحالة بعد دمج كل الأدلة",
  "claim_interpretation": "ما الذي يحتاجه فعلياً",
  "evidence_map": {{
    "text": ["..."],
    "images": ["..."],
    "search": ["..."]
  }},
  "consistency_check": {{
    "consistent": true/false,
    "conflicts": ["..."],
    "missing_gaps": ["..."]
  }},
  "severity_assessment": "طارئ جداً | متوسط | عادي",
  "final_decision": "صرف دعم / رفض / طلب معلومات إضافية / تحويل",
  "reasoning": "شرح مترابط يوضح كيف تم ربط جميع الأدلة للوصول للقرار"
}}
"""

    response = llm_model.invoke([
        HumanMessage(content=prompt)
    ])

    return {
        "final_output": response.content
    }