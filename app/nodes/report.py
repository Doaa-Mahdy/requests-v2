from app.services.llm import llm


def report_node(state: dict):

    prompt = f"""
أنت نظام تحليل وإعداد تقارير لصالح مؤسسة خيرية إنسانية.

---

🧭 السياق:
- الحالات لأشخاص مسجلين مسبقًا كمحتاجين.
- تم التحقق سابقًا من حالتهم الاجتماعية (فقر / دخل محدود).
- الطلب الحالي هو دعم إضافي طارئ بسبب ظرف جديد (مرض، حادث، أزمة مالية).

⚠️ لا تعيد تقييم الأهلية الأساسية.
ركّز فقط على "ما الذي يجب فعله الآن لمساعدة الحالة بأفضل طريقة".

---

🎯 هدفك:
مساعدة موظف المؤسسة على:
- فهم الحالة بوضوح
- تقييم درجة الاستعجال
- اتخاذ قرار مناسب
- اقتراح أفضل طريقة للدعم (وليس فقط قبول/رفض)

---

📊 المدخلات:

📌 الحالة:
{state["normalized_case"]}

📎 الأدلة:
{state["evidence"]}

🧠 التحليل:
{state["reasoning"]}

🧪 سجل التحقيق:
{state["reasoning"].get("inquiry_history", []) if isinstance(state.get("reasoning"), dict) else state.get("reasoning")}

---

⚖️ منطق التقييم:

- طارئ جدًا:
  خطر حياة / علاج عاجل → تدخل فوري

- متوسط:
  احتياج مهم لكنه غير مهدد للحياة → دعم جزئي أو مراجعة سريعة

- ضعيف:
  نقص أدلة أو غموض → طلب معلومات إضافية قبل القرار

---

🧠 المطلوب:

أنت لا تصدر حكم فقط، بل تساعد في القرار والإجراء.

أخرج JSON فقط:

{{
  "case_summary": "ملخص واضح للحالة",
  "urgent_need": "ما الاحتياج الطارئ بالتحديد",
  "severity": "منخفض | متوسط | عالي | طارئ",

  "key_evidence": "أهم الأدلة المؤثرة في القرار",
  "concerns": ["نقاط ضعف أو نقص في المعلومات"],

  "recommended_action": "ماذا يجب أن تفعل المؤسسة الآن (قرار + إجراء)",

  "support_options": [
    "مساعدة مالية كاملة",
    "دعم جزئي",
    "تحويل لمستشفى / جهة",
    "طلب مستندات إضافية",
    "متابعة اجتماعية"
  ],

  "suggested_next_steps": [
    "خطوة 1",
    "خطوة 2"
  ],

  "confidence": 0.0,
  "admin_summary": "ملخص إداري يساعد الموظف على اتخاذ القرار بسرعة"
}}

---

📌 قواعد صارمة:
- اعتمد فقط على البيانات المقدمة
- لا تفترض معلومات غير موجودة
- لا تعيد تقييم أهلية الفقر السابقة
- ركز على القرار + الإجراء وليس الحكم فقط
- اللغة العربية فقط
- JSON فقط بدون أي نص إضافي
"""

    response = llm.invoke(prompt)

    return {
        "final_output": response.content
    }